package com.g_box.springboot.pojo;

public class History {

	private int id;
	private int hisid;
	private String time;
	private String content;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getHobid() {
		return hisid;
	}
	public void setHobid(int hisid) {
		this.hisid = hisid;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}
