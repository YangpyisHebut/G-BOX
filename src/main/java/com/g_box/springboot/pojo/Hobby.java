package com.g_box.springboot.pojo;

public class Hobby {
	
	private int id;
	private int hobid;
	private String type;
	private String team;
	private String person;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getHobid() {
		return hobid;
	}
	public void setHobid(int hobid) {
		this.hobid = hobid;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getTeam() {
		return team;
	}
	public void setTeam(String team) {
		this.team = team;
	}
	public String getPerson() {
		return person;
	}
	public void setPerson(String person) {
		this.person = person;
	}
}
